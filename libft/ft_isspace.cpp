#include "libft.h"

int		ft_isspace(int c)
{
	if (c == ' ' || c == '\t' || c == '\n')
		return (TRUE);
	else if (c == '\v' || c == '\r' || c == '\f')
		return (TRUE);
	else
		return (FALSE);
}
